    private void jbSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSearchActionPerformed
        aparienceTF();
        if (!jTextField1.getText().trim().equals("")) {
            try {
                Map<String, Term> c = bdc.consulta(jTextField1.getText());
                String animal = jTextField1.getText().trim().toLowerCase();
                String d = c.get("D").toString().replace("[", "").replace("]", ".").replaceAll("'", "");
                d = d.replaceFirst(d.substring(0, 1), d.substring(0, 1).toUpperCase());
                String tipo = c.get("T").toString().replace("'", "");
                System.out.println(tipo);
                String caract = c.get("C").toString().replace("'", "");

                jlname.setText(animal.replaceFirst(animal.substring(0, 1), animal.substring(0, 1).toUpperCase()));
                jldescription.setText(d);
                jlclasificacion.setText("Es un " + tipo.replace("_", " ") + " " + caract + ".");

                jlImage.setIcon(new ImageIcon("filo/" + tipo + "/" + spacialChar(animal) + ".png"));
                try {
                    BufferedImage bf = ImageIO.read(new File("filo/" + tipo + "/" + animal + ".png"));
                Color color = new Color(bf.getRGB(bf.getWidth()/2, bf.getHeight()/2));
                if (color .getRGB() > -924188)
                {System.out.println(color.getRGB());
                    color  = Color.BLACK;
                    System.out.println("color");
                }
                    jlname.setForeground(color );
                } catch (IOException ex) {
                    System.out.println("No se encontro foto");
                }
            } catch (NoEncontradoException ex) {
                
                jlname.setText("Animal no encontrado.");
                jldescription.setText("");
                jlclasificacion.setText("");
                jlImage.setIcon(new ImageIcon("filo/no encontrado.png"));
                System.out.println("No encontro ningun animal");
            }
        }
    }//GEN-LAST:event_jbSearchActionPerformed

