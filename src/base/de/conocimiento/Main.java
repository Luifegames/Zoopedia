 private String spacialChar(String text){
        char []sc = {'á','é','í','ó','ù'};
        char [] c = {'a','e','i','o','o'};
        
        for (int i = 0; i < c.length; i++) {
            text = text.replace(sc[i], c[i]);
        }
        System.out.println(text);
        return text;
    }
